
// User defined vertex
public class DominoVertex {
	
	String top = "";
	String bottom = "";
	
	public DominoVertex() {}
	
	public DominoVertex(String _top, String _bottom) {
		top = _top;
		bottom = _bottom;
	}

	public String getTop() {
		return top;
	}

	public void setTop(String top) {
		this.top = top;
	}

	public String getBottom() {
		return bottom;
	}

	public void setBottom(String buttom) {
		this.bottom = buttom;
	}
	
	public String toString() {
		return "[" + top + "/" + bottom + "]";
	}
}
